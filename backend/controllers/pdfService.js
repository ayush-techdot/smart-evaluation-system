const { PDFDocument, rgb, StandardFonts } = require('pdf-lib');
const fs = require('fs');
const path = require('path');

const generatePDF = async ({ studentName, rollNumber, examTitle, subject, gradingResults, totalMarks, maxTotalMarks }) => {
  const pdfDoc = await PDFDocument.create();
  const font = await pdfDoc.embedFont(StandardFonts.Helvetica);
  const boldFont = await pdfDoc.embedFont(StandardFonts.HelveticaBold);

  const page = pdfDoc.addPage([595, 842]); // A4
  const { width, height } = page.getSize();
  let y = height - 40;

  const colors = {
    primary: rgb(0.1, 0.1, 0.4),
    accent: rgb(0.2, 0.5, 0.9),
    green: rgb(0.1, 0.6, 0.3),
    red: rgb(0.8, 0.2, 0.2),
    gray: rgb(0.5, 0.5, 0.5),
    lightGray: rgb(0.9, 0.9, 0.9),
    white: rgb(1, 1, 1),
    dark: rgb(0.1, 0.1, 0.1),
  };

  const drawLine = (yPos) => {
    page.drawLine({
      start: { x: 40, y: yPos },
      end: { x: width - 40, y: yPos },
      thickness: 0.5,
      color: colors.gray,
    });
  };

  const writeText = (text, x, yPos, opts = {}) => {
    page.drawText(String(text), {
      x, y: yPos,
      size: opts.size || 10,
      font: opts.bold ? boldFont : font,
      color: opts.color || colors.dark,
      maxWidth: opts.maxWidth || width - 80,
    });
  };

  // Header bar
  page.drawRectangle({ x: 0, y: height - 80, width, height: 80, color: colors.primary });
  writeText('STUDENT EVALUATION REPORT', 40, height - 35, { size: 18, bold: true, color: colors.white });
  writeText('Smart Evaluation System', 40, height - 55, { size: 10, color: rgb(0.7, 0.7, 1) });
  y = height - 100;

  // Student info box
  page.drawRectangle({ x: 40, y: y - 70, width: width - 80, height: 70, color: colors.lightGray });
  writeText(`Name: ${studentName}`, 55, y - 20, { bold: true, size: 12 });
  writeText(`Roll No: ${rollNumber}`, 55, y - 38, { size: 10, color: colors.gray });
  writeText(`Exam: ${examTitle}`, 300, y - 20, { bold: true, size: 12 });
  writeText(`Subject: ${subject}`, 300, y - 38, { size: 10, color: colors.gray });

  // Total score
  const percentage = maxTotalMarks > 0 ? Math.round((totalMarks / maxTotalMarks) * 100) : 0;
  const scoreColor = percentage >= 70 ? colors.green : percentage >= 40 ? colors.accent : colors.red;
  writeText(`Total Score: ${totalMarks} / ${maxTotalMarks}  (${percentage}%)`, 55, y - 58, {
    bold: true, size: 13, color: scoreColor,
  });

  y -= 90;
  drawLine(y);
  y -= 15;

  // Column headers
  writeText('Q#', 40, y, { bold: true, size: 9, color: colors.gray });
  writeText('Question', 70, y, { bold: true, size: 9, color: colors.gray });
  writeText('Marks', width - 120, y, { bold: true, size: 9, color: colors.gray });
  y -= 8;
  drawLine(y);
  y -= 12;

  // Question results
  for (const result of gradingResults) {
    // Check if we need a new page
    if (y < 100) {
      const newPage = pdfDoc.addPage([595, 842]);
      y = 800;
    }

    const markColor = result.marksAwarded === result.maxMarks
      ? colors.green
      : result.marksAwarded === 0 ? colors.red : colors.accent;

    writeText(`Q${result.questionNumber}`, 40, y, { bold: true, size: 10 });

    // Question text (truncated)
    const qText = result.questionText.length > 55
      ? result.questionText.substring(0, 52) + '...'
      : result.questionText;
    writeText(qText, 70, y, { size: 10 });
    writeText(`${result.marksAwarded} / ${result.maxMarks}`, width - 120, y, {
      bold: true, size: 10, color: markColor,
    });

    y -= 16;

    // Student answer preview (first 100 chars)
    const answerPreview = result.studentAnswer
      ? result.studentAnswer.substring(0, 90) + (result.studentAnswer.length > 90 ? '...' : '')
      : 'No answer';
    writeText(`Answer: ${answerPreview}`, 70, y, { size: 8, color: colors.gray, maxWidth: width - 160 });
    y -= 14;

    // Feedback
    const feedbackLines = result.feedback.match(/.{1,85}/g) || [];
    feedbackLines.forEach((line) => {
      writeText(`Feedback: ${line}`, 70, y, { size: 8, color: colors.primary, maxWidth: width - 160 });
      y -= 13;
    });

    drawLine(y);
    y -= 12;
  }

  // Footer
  y = 35;
  drawLine(y + 12);
  writeText(`Generated by Smart Eval • ${new Date().toLocaleDateString('en-IN', { day: '2-digit', month: 'long', year: 'numeric' })}`, 40, y, {
    size: 8, color: colors.gray,
  });

  const pdfBytes = await pdfDoc.save();
  const fileName = `report_${rollNumber}_${Date.now()}.pdf`;
  const outputPath = path.join(__dirname, '../uploads', fileName);
  fs.writeFileSync(outputPath, pdfBytes);

  return { fileName, filePath: outputPath };
};

module.exports = { generatePDF };
